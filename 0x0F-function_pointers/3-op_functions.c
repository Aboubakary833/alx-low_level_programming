/**
 * op_add - Addition two int
 * @a: First int
 * @b: Second int
 * Return: int on Success
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - Calculate the different
 * @a: First int
 * @b: Second int
 * Return: int on Success
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - Multiply two int
 * @a: First int
 * @b: Second int
 * Return: int on Success
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - Do a division
 * @a: First int
 * @b: Second int
 * Return: int on Success
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - Calculate a modulo
 * @a: First int
 * @b: Second int
 * Return: int on Success
*/
int op_mod(int a, int b)
{
return (a % b);
}
